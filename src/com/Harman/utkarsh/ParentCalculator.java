package com.Harman.utkarsh;

public class ParentCalculator {

    public int AdditionOperation(int x,int y)
    {
        int z = x + y;
        return z;
    }

    public int SubOperation(int x,int y )
    {
        int z = x-y;
        return z;
    }

    public int Multiplication(int x,int y )
    {
        int z = x*y;
        return z;
    }


}
